package ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain.sys;

import java.util.Date;

/**
 * Created by vostap on 24.09.2014.
 */
public class SysCounterStatus {
    private Long id;
    private Long counterID;
    private Date period;
    private Date statusDate;
    private Long docRegisterCounterID;
    private Long docUnregisterCounterID;
    private Long statusTypeID;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getCounterID() {
        return counterID;
    }

    public void setCounterID(Long counterID) {
        this.counterID = counterID;
    }

    public Date getPeriod() {
        return period;
    }

    public void setPeriod(Date period) {
        this.period = period;
    }

    public Date getStatusDate() {
        return statusDate;
    }

    public void setStatusDate(Date statusDate) {
        this.statusDate = statusDate;
    }

    public Long getDocRegisterCounterID() {
        return docRegisterCounterID;
    }

    public void setDocRegisterCounterID(Long docRegisterCounterID) {
        this.docRegisterCounterID = docRegisterCounterID;
    }

    public Long getDocUnregisterCounterID() {
        return docUnregisterCounterID;
    }

    public void setDocUnregisterCounterID(Long docUnregisterCounterID) {
        this.docUnregisterCounterID = docUnregisterCounterID;
    }

    public Long getStatusTypeID() {
        return statusTypeID;
    }

    public void setStatusTypeID(Long statusTypeID) {
        this.statusTypeID = statusTypeID;
    }
}
