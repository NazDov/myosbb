package ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain;

import java.util.Date;

/**
 * Created by vostap on 02.12.2014.
 */
public class CSCounterStatus {
    private Counter counter;
    private Date dateLastRegister;
    private String counterStatus;
    private Long counterStatusID;

    public CSCounterStatus(){
        counter = new Counter();
    }

    public Date getDateLastRegister() {
        return dateLastRegister;
    }

    public void setDateLastRegister(Date dateLastRegister) {
        this.dateLastRegister = dateLastRegister;
    }

    public String getCounterStatus() {
        return counterStatus;
    }

    public void setCounterStatus(String counterStatus) {
        this.counterStatus = counterStatus;
    }

    public Counter getCounter() {
        return counter;
    }

    public void setCounter(Counter counter) {
        this.counter = counter;
    }

    public Long getCounterStatusID() {
        return counterStatusID;
    }

    public void setCounterStatusID(Long counterStatusID) {
        this.counterStatusID = counterStatusID;
    }
}
