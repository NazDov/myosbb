public class InternalTurnOver extends ComboBoxItem{
    private Long id;
    private String name;

    public InternalTurnOver() {

    }

    public InternalTurnOver(Long id) {
        super(id);
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    @Override
    public String toString() {
        return "InternalTurnOver{" +
                "id=" + id +
                ", Name='" + name + '\'' +
                '}';
    }
}
