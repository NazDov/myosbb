public class ComboBoxItem {
    protected Long id;
    protected String name;

    public ComboBoxItem(){}

    public ComboBoxItem(Long id){
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "ComboBoxItem{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
