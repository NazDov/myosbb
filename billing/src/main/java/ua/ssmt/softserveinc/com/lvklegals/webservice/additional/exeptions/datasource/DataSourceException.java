package ua.ssmt.softserveinc.com.lvklegals.webservice.additional.exeptions.datasource;

import ua.ssmt.softserveinc.com.lvklegals.webservice.additional.exeptions.StorageException;

/**
 * Created by vostap on 23.08.2014.
 */
public class DataSourceException extends StorageException {
    public DataSourceException() {
    }

    public DataSourceException(String message) {
        super(message);
    }

    public DataSourceException(String message, Throwable cause) {
        super(message, cause);
    }

    public DataSourceException(Throwable cause) {
        super(cause);
    }

    public DataSourceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
