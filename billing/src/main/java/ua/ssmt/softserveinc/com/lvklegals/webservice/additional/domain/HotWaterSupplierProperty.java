public class HotWaterSupplierProperty {
    private Long id;
    private String bankAccount;
    private String fullName;
    private String shortName;
    private Double limitConsuming;
    private Bank bank;
    private GroupWatterSupply groupWatterSupply;
    private TypeActivity typeActivity;
    private SourceWaterSupply sourceWaterSupply;
    private ContraAgentCategory contraAgentCategory;




    public HotWaterSupplierProperty() {
        this.groupWatterSupply = new GroupWatterSupply();
        this.typeActivity = new TypeActivity();
        this.sourceWaterSupply = new SourceWaterSupply();
        this.contraAgentCategory = new ContraAgentCategory();

    }

    public HotWaterSupplierProperty(Long id) {
        this();
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public GroupWatterSupply getGroupWatterSupply() {
        return groupWatterSupply;
    }

    public void setGroupWatterSupply(GroupWatterSupply groupWatterSupply) {
        this.groupWatterSupply = groupWatterSupply;
    }

    public TypeActivity getTypeActivity() {
        return typeActivity;
    }

    public void setTypeActivity(TypeActivity typeActivity) {
        this.typeActivity = typeActivity;
    }

    public SourceWaterSupply getSourceWaterSupply() {
        return sourceWaterSupply;
    }

    public void setSourceWaterSupply(SourceWaterSupply sourceWaterSupply) {
        this.sourceWaterSupply = sourceWaterSupply;
    }

    public ContraAgentCategory getContraAgentCategory() {
        return contraAgentCategory;
    }

    public void setContraAgentCategory(ContraAgentCategory contraAgentCategory) {
        this.contraAgentCategory = contraAgentCategory;
    }



    public String getBankAccount() {
        return bankAccount;
    }

    public void setBankAccount(String bankAccount) {
        this.bankAccount = bankAccount;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getShortName() {
        return shortName;
    }

    public void setShortName(String shortName) {
        this.shortName = shortName;
    }


    public Double getLimitConsuming() {
        return limitConsuming;
    }

    public void setLimitConsuming(Double limitConsuming) {
        this.limitConsuming = limitConsuming;
    }

    public Bank getBank() {
        return bank;
    }

    public void setBank(Bank bank) {
        this.bank = bank;
    }
}
