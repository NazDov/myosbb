package ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain.documents;

import ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain.Counter;
import ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain.Department;
import ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain.Input;

import java.util.Date;

/**
 * Created by vostap on 08.10.2014.
 */
public class InputFactor {

    private Long id;
    private Date onDate;
    private Counter counter;
    private int beginFactor;
    private int endFactor;
    private int volume;
    private Long documentID;
    private Long unregisterDocumentID;
    private Long wiringID;
    private Long correctionDocumentID;
    private Input input;
    private int factorOnBeginMonth;
    private Date period;
    private String codeConto;
    private Department department;
    private String agreement;
    private String conto;
    private String  counterModel;
    private Date inputDate;
    private Long factorTypeID;
    private int doctype = -1;

    public InputFactor(){
        this.counter = new Counter();

    }

    public InputFactor(Long id){
        this();
        this.id = id;
    }

    public Counter getCounter() {
        return counter;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setCounter(Counter counter) {
        this.counter = counter;
    }

    public Integer getBeginFactor() {
        return beginFactor;
    }

    public void setBeginFactor(Integer beginFactor) {
        this.beginFactor = beginFactor;
    }

    public Integer getEndFactor() {
        return endFactor;
    }

    public void setEndFactor(Integer endFactor) {
        this.endFactor = endFactor;
    }

    public Integer getVolume() {
        return volume;
    }

    public void setVolume(Integer volume) {
        this.volume = volume;
    }

    public Long getDocumentID() {
        return documentID;
    }

    public void setDocumentID(Long documentID) {
        this.documentID = documentID;
    }

    public Long getWiringID() {
        return wiringID;
    }

    public void setWiringID(Long wiringID) {
        this.wiringID = wiringID;
    }

    public Long getUnregisterDocumentID() {
        return unregisterDocumentID;
    }

    public void setUnregisterDocumentID(Long unregisterDocumentID) {
        this.unregisterDocumentID = unregisterDocumentID;
    }

    public Long getCorrectionDocumentID() {
        return correctionDocumentID;
    }

    public void setCorrectionDocumentID(Long correctionDocumentID) {
        this.correctionDocumentID = correctionDocumentID;
    }

    public Input getInput() {
        return input;
    }

    public void setInput(Input input) {
        this.input = input;
    }

    public String getCodeConto() {
        return codeConto;
    }

    public void setCodeConto(String codeConto) {
        this.codeConto = codeConto;
    }

    public Department getDepartment() {
        return department;
    }

    public void setDepartment(Department department) {
        this.department = department;
    }

    public String getAgreement() {
        return agreement;
    }

    public void setAgreement(String agreement) {
        this.agreement = agreement;
    }

    public String getConto() {
        return conto;
    }

    public void setConto(String conto) {
        this.conto = conto;
    }

    @Override
    public String toString() {
        return "InputFactor{" +

                ", beginFactor=" + beginFactor +
                ", endFactor=" + endFactor +
                ", volume=" + volume +
                "counter=" + counter +
                "counterModel=" + counterModel +
                '}';
    }

    public Integer getFactorOnBeginMonth() {
        return factorOnBeginMonth;
    }

    public void setFactorOnBeginMonth(Integer factorOnBeginMonth) {
        this.factorOnBeginMonth = factorOnBeginMonth;
    }

    public Date getPeriod() {
        return period;
    }

    public void setPeriod(Date period) {
        this.period = period;
    }

    public String getCounterModel() {
        return counterModel;
    }

    public void setCounterModel(String counterModel) {
        this.counterModel = counterModel;
    }

    public Date getInputDate() {
        return inputDate;
    }

    public void setInputDate(Date inputDate) {
        this.inputDate = inputDate;
    }

    public Long getFactorTypeID() {
        return factorTypeID;
    }

    public void setFactorTypeID(Long factorTypeID) {
        this.factorTypeID = factorTypeID;
    }

    public Date getOnDate() {
        return onDate;
    }

    public void setOnDate(Date onDate) {
        this.onDate = onDate;
    }

    public int getDoctype() {
        return doctype;
    }

    public void setDoctype(int doctype) {
        this.doctype = doctype;
    }
}
