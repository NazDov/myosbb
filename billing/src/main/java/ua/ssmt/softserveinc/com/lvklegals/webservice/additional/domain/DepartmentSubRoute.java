package ua.ssmt.softserveinc.com.lvklegals.webservice.additional.domain;

import java.util.Date;

/**
 * Created by vostap on 21.10.2014.
 */
public class DepartmentSubRoute {
    private Long id;
    private Department department;
    private SubRoute subRoute;
    private Date period;

    public DepartmentSubRoute(){
        this.department = new Department();
        this.subRoute = new SubRoute();
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Department getDepartment() {
        return department;
    }

    public void setDepartment(Department department) {
        this.department = department;
    }

    public SubRoute getSubRoute() {
        return subRoute;
    }

    public void setSubRoute(SubRoute subRoute) {
        this.subRoute = subRoute;
    }

    public Date getPeriod() {
        return period;
    }

    public void setPeriod(Date period) {
        this.period = period;
    }
}
